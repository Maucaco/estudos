package aulinha;

public class Arrays {
	public static void main(String[] args) {

		String [] nome = new String [5];
			nome[0] = "Mauricio";
			nome[1] = "Sarah";
			
			System.out.println(nome[0]);
			System.out.println(nome[1]);
			
		String [][] var2 = new	String [5][5];
				var2[0][0] = "mauricio";
		        var2[1][1] = "sarah";
		        
		        System.out.println (var2[0][0]);
		        System.out.println (var2[1][1]);
	
	
	
	}

}
